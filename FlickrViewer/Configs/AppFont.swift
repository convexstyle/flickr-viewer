/**
 Manage app level font type.
 */
enum AppFont: String {
  case Thin = "HelveticaNeue-Thin"
  case Light = "HelveticaNeue-Light"
  case Normal = "HelveticaNeue"
  case Medium = "HelveticaNeue-Medium"
  case Bold = "HelveticaNeue-Bold"
}